    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        // TODO add your handling code here:
        Connection c = null;

        Statement stmt = null;

        String consulta;

        try {
            Class.forName("org.postgresql.Driver");

            c = DriverManager.getConnection("jdbc:postgresql://localhost:5432/ejemplo", "postgres", "123456");
            stmt = c.createStatement();
            consulta = "select * from persona where rut='"+ jTextField1.getText() + "'";
            System.out.println(consulta);
            ResultSet rs = stmt.executeQuery(consulta);

            if (rs.next()) {

                String r = rs.getString("rut");
                String n = rs.getString("nombre");
                String a = rs.getString("apellido");
                int d = rs.getInt("edad");

                jTextField1.setText(r.trim());
                jTextField2.setText(n.trim());
                jTextField3.setText(a.trim());
                jTextField4.setText(valueOf(d).trim());

                jButton3.setEnabled(true);
                jButton4.setEnabled(true);

                //System.out.println("rut : "+ r + " Nombre :" + n + " Apellido :" + a + "Edad :" + d);

            } else {
                JOptionPane.showMessageDialog(null, "Rut no encontadro");
                jButton2.setEnabled(true);

            }

            rs.close();

            stmt.close();

            //c.commit();
            c.close();

        } catch (Exception e) {

            JOptionPane.showMessageDialog(null, "Error al conectarse con la bdd");
        }
    }//GEN-LAST:event_jButton1MouseClicked

